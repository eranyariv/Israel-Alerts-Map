/**
 * Returns [lat, lon] for a city name, or null.
 * Falls back to partial matching if exact match fails.
 */
export function getCityCoords(name) {
  if (!name) return null
  if (CITY_COORDS[name]) return CITY_COORDS[name]
  // Partial match
  for (const [key, coords] of Object.entries(CITY_COORDS)) {
    if (name.includes(key) || key.includes(name)) return coords
  }
  return null
}
